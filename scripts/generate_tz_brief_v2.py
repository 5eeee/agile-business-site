# -*- coding: utf-8 -*-
"""Генерация брифа для ТЗ на основе принципов из прошлой итерации."""
from docx import Document
from docx.shared import Pt

def build():
    doc = Document()
    style = doc.styles["Normal"]
    style.font.name = "Calibri"
    style.font.size = Pt(11)

    def hint(text):
        para = doc.add_paragraph()
        r = para.add_run("Подсказка: ")
        r.italic = True
        para.add_run(text).italic = True

    def qa(q, placeholder):
        doc.add_paragraph(q, style="List Number")
        doc.add_paragraph(placeholder, style="List Bullet 2")

    t = doc.add_paragraph()
    r = t.add_run("Бриф для подготовки технического задания (ТЗ)")
    r.bold = True
    r.font.size = Pt(16)
    doc.add_paragraph("Версия 2.0 · для заказчика и команды разработки")

    doc.add_paragraph()
    doc.add_paragraph(
        "Этот документ помогает собрать ответы у заказчика так, чтобы потом из них получилось понятное ТЗ. "
        "Формулировки максимально «человеческие»: технические детали команда доработает сама."
    )
    hint("Если чего‑то не знаете — пишите «не знаю / решить с подрядчиком». Это нормально и лучше, чем угадывать.")

    doc.add_heading("Как работать с шаблоном", level=1)
    for b in (
        "Сначала заполните разделы 1–3: зачем продукт и кто им пользуется.",
        "Потом — что именно делаем (тип продукта) и сценарии.",
        "Детальные блоки (оплата, личный кабинет и т.д.) отмечайте только если относятся к вашему проекту.",
        "В конце раздела «Для разработчика» — как превратить ответы в формулировки требований.",
    ):
        doc.add_paragraph(b, style="List Bullet")

    doc.add_heading("0. Карточка проекта", level=1)
    qa("Название проекта (рабочее)", "[например: сайт компании «Ромашка»]")
    qa("Заказчик, контактное лицо, роль", "[ФИО, должность, телефон, email]")
    qa("Кто ещё должен быть в копии / на согласовании", "[бухгалтерия, маркетинг, IT — если есть]")

    doc.add_heading("1. Зачем это нужно бизнесу (цели и успех)", level=1)
    qa(
        "Опишите одной-двумя фразами: что должно появиться в итоге",
        "[новый сайт / приложение / личный кабинет / интеграция …]",
    )
    qa(
        "Какую проблему или неудобство это убирает сегодня?",
        "[например: заявки теряются в переписке, нет онлайн-оплаты, сайт не открывается с телефона …]",
    )
    qa(
        "Как вы поймёте, что проект удался? (метрики или признаки)",
        "[если есть цифры — укажите; если нет — опишите словами: «стало проще оформлять заказ», "
        "«меньше звонков с одними и теми же вопросами» и т.п.]",
    )
    hint(
        "Вопрос не про «обязательные KPI». Если цифр нет — фиксируем качественный результат и договоримся о приёмке позже."
    )
    qa(
        "Жёсткие рамки: сроки, бюджет, законы, внутренние регламенты",
        "[что нельзя нарушать ни при каких условиях]",
    )

    doc.add_heading("2. Продукт: что именно делаем", level=1)
    qa(
        "Тип продукта (можно несколько)",
        "[сайт · мобильное приложение · веб-сервис · личный кабинет · админ-панель · чат-бот · "
        "доработка существующей системы · другое]",
    )
    qa(
        "Если это сайт — какой он по смыслу",
        "[визитка · корпоративный · каталог · интернет-магазин · лендинг · портал · другое]",
    )
    qa(
        "Главная задача продукта для пользователя",
        "[купить · оставить заявку · найти информацию · записаться · обучиться · работать с личными данными · другое]",
    )
    qa("Языки интерфейса и контента", "[русский / английский / другие; нужен ли переключатель; нужен ли автоперевод]")
    qa("Доменное имя (если уже есть или планируется)", "[example.ru или «подобрать с подрядчиком»]")
    hint(
        "Блок «хостинг, PHP, база данных» лучше не заполнять заказчику, если он не уверен. "
        "Достаточно: «есть ли уже хостинг/сервер, у кого администрируется»."
    )
    qa(
        "Инфраструктура (только если знаете)",
        "[уже есть хостинг/сервер — у кого · нет — ждём рекомендацию · есть корпоративные ограничения]",
    )

    doc.add_heading("3. Аудитория и сценарии (без техники)", level=1)
    qa(
        "Кто основные пользователи",
        "[частные лица / компании / сотрудники / партнёры; возраст и контекст «когда обращаются» — по возможности]",
    )
    qa(
        "Есть ли разные роли (гость, клиент, менеджер, админ)",
        "[перечислите; если одна роль — так и напишите]",
    )
    qa(
        "3–5 типичных ситуаций «как в жизни»",
        "[например: клиент с телефона оформляет заказ; менеджер выгружает отчёт …]",
    )
    hint("Это заменяет длинные списки «целевая аудитория» из разных разделов одним местом.")

    doc.add_heading("4. Структура и экраны", level=1)
    qa(
        "Карта разделов / страниц / экранов (дерево)",
        "[Главная → О компании → … или приложите схему/скрин]",
    )
    qa(
        "Есть ли текущий сайт или продукт, который заменяем",
        "[ссылка; что обязательно сохранить по структуре или URL]",
    )

    doc.add_heading("4.1. Детализация по каждой ключевой странице или экрану", level=2)
    doc.add_paragraph(
        "Скопируйте блок ниже для каждой важной страницы (или приложите таблицу Excel). На интеграции со страницы "
        "достаточно сослаться на раздел 6 («Интеграции»), без повторного описания."
    )
    for line in (
        "Название страницы: …",
        "Зачем она нужна (цель страницы): …",
        "Что на ней должно быть (текст, фото, список, кнопки, форма, карта): …",
        "Что пользователь может сделать (нажать, заполнить, скачать): …",
        "Кто видит страницу (все / только после входа / только роль X): …",
        "Приоритет для первой версии (обязательно / желательно / позже): …",
    ):
        doc.add_paragraph(line, style="List Bullet")

    doc.add_heading("5. Функции и сценарии заказа (отметьте нужное)", level=1)
    doc.add_paragraph("Отмечайте только то, что относится к проекту. Лишнее не заполняйте.")

    doc.add_heading("5.1. Поиск и подбор", level=3)
    qa("Нужен ли поиск / фильтры / сортировка", "[по каким полям; пример: название, цена, артикул]")

    doc.add_heading("5.2. Формы и заявки", level=3)
    qa("Какие формы нужны и какие поля", "[имя, телефон, email, файл, комментарий …]")
    qa("Куда уходят заявки", "[email · CRM · мессенджер · таблица — если знаете]")

    doc.add_heading("5.3. Личный кабинет", level=3)
    qa("Нужен ли личный кабинет; кто входит и что делает", "[клиент: история заказов; менеджер: цены — если актуально]")

    doc.add_heading("5.4. Корзина, оплата, доставка (для магазинов)", level=3)
    qa(
        "Как оформляется заказ; способы оплаты и доставки",
        "[карта, счёт, наличные; курьер, самовывоз, почта]",
    )

    doc.add_heading("5.5. Прочий функционал", level=3)
    qa(
        "Дополнительно",
        "[калькулятор, чат, онлайн-запись, сравнение товаров, подписка на рассылку — что угодно релевантное]",
    )

    doc.add_heading("5.6. Приоритеты (MoSCoW)", level=3)
    doc.add_paragraph(
        "Must — без этого не запускаем; Should — важно, но можно второй очередью; Could — если останется время; "
        "Won't — точно не в этой версии."
    )
    doc.add_paragraph("[Заполните списком или в таблице]", style="List Bullet")

    doc.add_heading("6. Интеграции и обмен данными (один раз для всего проекта)", level=1)
    qa(
        "С какими системами должен «общаться» продукт",
        "[1С, CRM, склад, касса, банк, почта, аналитика, реклама — что применимо]",
    )
    qa("Какие данные должны уходить или приходить", "[заявки, остатки, статусы заказов, клиенты — по возможности]")
    qa("Есть ли API, доступы, ответственные с той стороны", "[если не знаете — «уточнить с IT»]")
    hint(
        "Этот раздел заменяет повтор одних и тех же вопросов про интеграции на уровне каждой страницы. "
        "На странице достаточно сослаться: «данные из интеграции X»."
    )

    doc.add_heading("7. Качество, устройства, безопасность (простыми словами)", level=1)

    doc.add_heading("7.1. Устройства и браузеры", level=3)
    qa("С чего чаще всего заходят пользователи", "[компьютер · планшет · телефон · всё сразу]")
    qa("Нужна ли полноценная мобильная версия (адаптив)", "[да / достаточно десктопа — редкость, обычно да]")
    qa(
        "Есть ли обязательные браузеры для корпоративных клиентов",
        "[Chrome, Safari, Edge, «старый IE не нужен» — если важно]",
    )

    doc.add_heading("7.2. Скорость и нагрузка", level=3)
    qa(
        "Ожидания по скорости",
        "[например: «страницы открываются без долгого ожидания»; конкретные секунды — по согласованию с разработчиком]",
    )
    hint(
        "Формулировка «как в статье при 5 Мбит/с» для заказчика неудачна: он может не понять. Лучше образно или оставить число с подрядчиком."
    )
    qa("Примерная посещаемость и всплески", "[заявок в день; пики в день акций]")

    doc.add_heading("7.3. Данные и правовые требования", level=3)
    qa("Какие персональные или чувствительные данные собираем", "[ФИО, телефон, адрес, паспорт, платёжи …]")
    qa("Нужны ли окна согласия на cookies и обработку данных", "[да/нет; есть ли готовые тексты у юристов]")
    qa("Нужны ли защита от спама (капча), вход по паролю на часть разделов", "[да/нет, где именно]")
    qa(
        "Нужен ли защищённый доступ по HTTPS (замочек в браузере)",
        "[обычно да для форм и личных кабинетов — отметьте ожидание]",
    )

    doc.add_heading("8. Дизайн, бренд, тон общения", level=1)
    qa("Логотип, фирменные цвета, шрифты", "[есть файлы / нужно разработать / частично есть]")
    qa(
        "Каким должен быть «характер» сайта",
        "[строго / дружелюбно / премиально / технологично; пример фразы «так надо / так не надо»]",
    )
    qa("Анимации и движение", "[минимум / умеренно / без ограничений]")
    qa(
        "Особые требования доступности",
        "[увеличение шрифта, контраст, версия для слабовидящих — если нужно]",
    )
    qa("Референсы (2–3 сайта) и что нравится / не нравится", "[ссылки + 1–2 предложения на каждый]")
    qa("Пример, где вам было удобно что-то сделать онлайн", "[ссылка или название + что именно было удобно]")
    hint("Нейтральный тон: «пришлите / приложите ссылки» — уместно и в B2B.")

    doc.add_heading("9. Компания и предложение (кратко для контента)", level=1)
    qa("Кто вы и чем занимаетесь (2–4 предложения)", "[…]")
    qa("Что продаёте или какие услуги главные", "[…]")
    qa("География, офис, склад — что показать на карте", "[…]")

    doc.add_heading("10. Контент, наполнение, миграция", level=1)
    qa("Кто в вашей компании добавляет и правит материалы на сайте", "[роли; один человек или несколько]")
    qa("Откуда берутся тексты, фото, видео", "[уже есть / нужен копирайтер/фотограф / частично]")
    qa("В каком формате и когда передадите материалы", "[Word, Excel, облако, сроки]")
    qa("Кто отвечает, что контент не нарушает чужие права", "[заказчик / совместно]")
    qa("Нужен ли перенос со старого сайта", "[да/нет; примерный объём страниц или товаров]")

    doc.add_heading("11. Согласование и приёмка", level=1)
    qa(
        "Как удобнее проверять результат",
        "[демо на тестовом адресе · по чек-листу · совместная сессия]",
    )
    qa(
        "Что для вас критично проверить перед запуском",
        "[формы, оплата, отображение в мобильном, интеграции …]",
    )

    doc.add_heading("Для разработчика: как перевести ответы в ТЗ", level=1)
    doc.add_paragraph("Рекомендуемая фиксация каждого требования:")
    for line in (
        "ID (например FR-001, NFR-001).",
        "Формулировка «Система должна …» или user story «Как <роль>, я хочу <действие>, чтобы <ценность».",
        "Приоритет (Must/Should/Could).",
        "Источник: цитата или ссылка на пункт брифа.",
        "Зависимости: интеграции, контент, доступы.",
        "Критерий приёмки: как проверим, что сделано.",
    ):
        doc.add_paragraph(line, style="List Bullet")

    doc.add_paragraph()
    doc.add_paragraph("Что изменено по сравнению с исходным «Шаблон для составления ТЗ»:")
    for line in (
        "Дубли: интеграции собраны в одном разделе 6; на странице — только ссылка на него (нет несуществующего «п. 2.5»).",
        "Нефункциональные требования (скорость, безопасность, устройства) вынесены в раздел 7, а не смешаны с перечнем функций.",
        "Сначала цели и аудитория (разделы 1–3), затем структура и функции — удобнее для discovery.",
        "Опечатка «запоняется» в исходнике: здесь явно «заполняется отдельно для каждой страницы».",
        "Жаргон и отсылка «как в статье» смягчены; KPI — без давления «только цифры».",
    ):
        doc.add_paragraph(line, style="List Bullet")

    return doc


def main():
    doc = build()
    project_path = r"c:\Users\vladi\AgileBusinessVisitca\Шаблон_бриф_ТЗ_версия_2.docx"
    doc.save(project_path)
    print("Saved:", project_path)

    docs_candidates = [
        r"C:\Users\vladi\Documents\Шаблон бриф-опросник для ТЗ (версия 2).docx",
        r"C:\Users\vladi\Documents\Шаблон бриф-опросник для ТЗ (версия 2) — новый.docx",
    ]
    for p in docs_candidates:
        try:
            doc.save(p)
            print("Saved:", p)
            break
        except OSError as e:
            print("Skip (locked or error):", p, "-", e)


if __name__ == "__main__":
    main()
